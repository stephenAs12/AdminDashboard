            jQuery("#country_").ready(function() {
                var countryValue = jQuery(this).val();
                jQuery.ajax({
                    url: "get-state.php",
                    method: "POST",
                    data: {
                        country_value: countryValue
                    },
                    success: function(data) {
                        jQuery("#state_").html(data);
                    }
                });
            });

            jQuery("#state_").change(function() {
                var stateValue = jQuery(this).val();
                jQuery.ajax({
                    url: "get-city.php",
                    method: "POST",
                    data: {
                        state_value: stateValue
                    },
                    success: function(data) {
                        jQuery("#city_").html(data);
                    }
                });
            });
        });
    </script>

</body>

</html>